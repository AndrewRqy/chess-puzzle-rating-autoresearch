"""
Per-run scorer for: Predicting Lichess Chess-Puzzle Difficulty Ratings from Position.

Generated by rule_maker. Measures: test_mae, spearman, middle_decile_calibration.

The scorer:
  1. Loads the runner's `predict(rows)` function from `src/predict.py`.
  2. Deterministically samples a sealed test slice from the local Lichess
     parquet shards (hash-based holdout on PuzzleId).
  3. Calls `predict` on the test rows and computes three scalar metrics.
  4. Compares each metric against the target in `scoring/targets.json` and
     writes the per-property record to `scoring/results.json`.

Uses stdlib + numpy + pandas + pyarrow (all present in the runner's
declared dependency set). No pipeline-specific helper imports.
"""
import json
import hashlib
import importlib.util
import math
from pathlib import Path
from typing import Any, Callable, Dict, List

WS = Path(__file__).resolve().parent.parent
EVAL_VERSION = 1

# Sealed test-slice parameters. Kept out of interface.md so the runner cannot
# reconstruct which puzzles land in the held-out set.
_TEST_HASH_MOD = 100
_TEST_HASH_MATCH = 37          # PuzzleId's md5 % 100 == 37  ->  test
_RD_MAX = 80                    # match the idea's "RatingDeviation < 80" filter
_TEST_CAP = 2000                # cap for scoring runtime; deterministic subsample
_TEST_SEED = 20260702           # deterministic sub-sample seed
_MIDDLE_ELO_LO = 1400
_MIDDLE_ELO_HI = 2000

# === BLOCK 1: boilerplate ===================================================

def _load_module(name: str, path: Path):
    spec = importlib.util.spec_from_file_location(name, path)
    if spec is None or spec.loader is None:
        raise FileNotFoundError(f"could not import {path}")
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


def _is_test_puzzle(puzzle_id: str) -> bool:
    h = int(hashlib.md5(puzzle_id.encode("utf-8")).hexdigest(), 16)
    return (h % _TEST_HASH_MOD) == _TEST_HASH_MATCH


def _load_sealed_test_rows() -> List[Dict[str, Any]]:
    """Read all local parquet shards, apply the sealed hash-based holdout,
    filter to low-RD puzzles, and return a list of row-dicts."""
    import pandas as pd  # local import: only needed at eval time

    shard_dir = WS / "datasets" / "lichess_puzzles" / "data"
    shards = sorted(shard_dir.glob("train-*.parquet"))
    if not shards:
        raise FileNotFoundError(f"no parquet shards under {shard_dir}")

    cols = ["PuzzleId", "FEN", "Moves", "Rating", "RatingDeviation", "Themes"]
    frames = []
    for shard in shards:
        df = pd.read_parquet(shard, columns=cols)
        df = df[df["RatingDeviation"] < _RD_MAX]
        # Hash filter is cheap enough to run on the full shard.
        mask = df["PuzzleId"].map(_is_test_puzzle)
        frames.append(df[mask])
    test_df = pd.concat(frames, ignore_index=True)

    # Cap deterministically for scoring runtime.
    if len(test_df) > _TEST_CAP:
        test_df = test_df.sample(n=_TEST_CAP, random_state=_TEST_SEED).reset_index(drop=True)

    rows: List[Dict[str, Any]] = []
    for rec in test_df.to_dict(orient="records"):
        themes = rec.get("Themes")
        if themes is None:
            themes = []
        elif hasattr(themes, "tolist"):
            themes = list(themes.tolist())
        else:
            themes = list(themes)
        rows.append({
            "PuzzleId": rec["PuzzleId"],
            "FEN": rec["FEN"],
            "Moves": rec["Moves"],
            "Themes": themes,
            "_Rating": float(rec["Rating"]),
        })
    return rows


def _spearman(a: List[float], b: List[float]) -> float:
    import numpy as np

    n = len(a)
    if n < 2:
        return 0.0

    def _rank(x: List[float]) -> List[float]:
        order = sorted(range(n), key=lambda i: x[i])
        ranks = [0.0] * n
        i = 0
        while i < n:
            j = i
            while j + 1 < n and x[order[j + 1]] == x[order[i]]:
                j += 1
            avg_rank = 0.5 * (i + j) + 1.0
            for k in range(i, j + 1):
                ranks[order[k]] = avg_rank
            i = j + 1
        return ranks

    ra = _rank(a)
    rb = _rank(b)
    ra_arr = np.asarray(ra, dtype=float)
    rb_arr = np.asarray(rb, dtype=float)
    ra_c = ra_arr - ra_arr.mean()
    rb_c = rb_arr - rb_arr.mean()
    denom = math.sqrt(float((ra_c * ra_c).sum()) * float((rb_c * rb_c).sum()))
    if denom == 0.0:
        return 0.0
    return float((ra_c * rb_c).sum() / denom)


# === BLOCK 2: property measurements =========================================

def measure_test_mae(preds: List[float], truths: List[float]) -> Dict[str, Any]:
    """Mean Absolute Error in Elo points on the sealed test slice."""
    n = len(preds)
    if n == 0:
        return {"value": float("inf"), "direction": "min"}
    mae = sum(abs(p - t) for p, t in zip(preds, truths)) / n
    return {"value": float(mae), "direction": "min"}


def measure_spearman(preds: List[float], truths: List[float]) -> Dict[str, Any]:
    """Spearman rank correlation between predicted and true ratings."""
    rho = _spearman(preds, truths)
    return {"value": float(rho), "direction": "max"}


def measure_middle_decile_calibration(
    preds: List[float], truths: List[float]
) -> Dict[str, Any]:
    """Largest |mean_pred - mean_true| within any 100-Elo bin whose bin
    center falls inside [1400, 2000]. Puzzles are bucketed by their TRUE
    rating (unknown to the runner) so a mean-predictor cannot game it."""
    if not preds:
        return {"value": float("inf"), "direction": "min"}

    bins: Dict[int, List[tuple]] = {}
    for p, t in zip(preds, truths):
        if t < _MIDDLE_ELO_LO or t >= _MIDDLE_ELO_HI:
            continue
        b = int(t // 100) * 100
        bins.setdefault(b, []).append((p, t))

    if not bins:
        return {"value": float("inf"), "direction": "min"}

    max_gap = 0.0
    for b, pairs in bins.items():
        if len(pairs) < 20:
            # Too few samples in this bin to be a reliable calibration signal.
            continue
        mean_pred = sum(p for p, _ in pairs) / len(pairs)
        mean_true = sum(t for _, t in pairs) / len(pairs)
        gap = abs(mean_pred - mean_true)
        if gap > max_gap:
            max_gap = gap
    return {"value": float(max_gap), "direction": "min"}


# === BLOCK 3: main ==========================================================

def _sanitize_predictions(raw: Any, n: int) -> List[float]:
    """Coerce the runner's output to a list[float] of length n. Reject NaN /
    inf. Falls back to raising ValueError with a diagnostic string."""
    if raw is None:
        raise ValueError("predict() returned None")
    try:
        preds = list(raw)
    except TypeError as e:
        raise ValueError(f"predict() output is not iterable: {e}")
    if len(preds) != n:
        raise ValueError(f"predict() returned {len(preds)} values; expected {n}")
    clean: List[float] = []
    for i, v in enumerate(preds):
        try:
            f = float(v)
        except (TypeError, ValueError) as e:
            raise ValueError(f"predict()[{i}] is not a float: {v!r} ({e})")
        if math.isnan(f) or math.isinf(f):
            raise ValueError(f"predict()[{i}] is NaN or infinite: {f}")
        clean.append(f)
    return clean


def main() -> None:
    targets = json.loads((WS / "scoring" / "targets.json").read_text())

    # Load the runner's artifact.
    runner_path = WS / "src" / "predict.py"
    runner_mod = _load_module("runner_predict", runner_path)
    if not hasattr(runner_mod, "predict"):
        raise AttributeError(f"{runner_path} does not define a `predict` function")
    predict_fn: Callable[[List[Dict[str, Any]]], List[float]] = runner_mod.predict

    # Load the sealed test slice.
    test_rows = _load_sealed_test_rows()
    truths = [row["_Rating"] for row in test_rows]

    # Strip the private label before passing to the runner.
    model_inputs = [
        {k: v for k, v in row.items() if not k.startswith("_")}
        for row in test_rows
    ]

    raw_preds = predict_fn(model_inputs)
    preds = _sanitize_predictions(raw_preds, len(test_rows))

    properties: Dict[str, Dict[str, Any]] = {}
    for name, measurer in [
        ("test_mae", lambda: measure_test_mae(preds, truths)),
        ("spearman", lambda: measure_spearman(preds, truths)),
        ("middle_decile_calibration",
         lambda: measure_middle_decile_calibration(preds, truths)),
    ]:
        p = measurer()
        tgt = targets[name]["target"]
        p["target"] = tgt
        if p["direction"] == "max":
            p["satisfied"] = p["value"] >= tgt
        else:
            p["satisfied"] = p["value"] <= tgt
        p["gap"] = p["value"] - tgt
        properties[name] = p

    failing = [(n, p) for n, p in properties.items() if not p["satisfied"]]
    bottleneck = (
        min(failing, key=lambda kv: abs(kv[1]["gap"]))[0]
        if failing
        else None
    )

    out = {
        "properties": properties,
        "overall_satisfied": all(p["satisfied"] for p in properties.values()),
        "bottleneck": bottleneck,
        "eval_meta": {
            "eval_version": EVAL_VERSION,
            "generated_by": "rule_maker",
            "n_test": len(test_rows),
            "rd_max": _RD_MAX,
            "middle_elo_range": [_MIDDLE_ELO_LO, _MIDDLE_ELO_HI],
        },
    }
    (WS / "scoring" / "results.json").write_text(json.dumps(out, indent=2))


if __name__ == "__main__":
    main()
